import path from "node:path";
import { buildGuidePages } from "./build-guides.mjs";
import { getFnllaWebManifest } from "./fnlla-web-manifest.mjs";
import { syncDocShells } from "./sync-doc-shells.mjs";
import {
  copyDirectory,
  getRepoRoot,
  isDirectExecution,
  pathExists,
  readText,
  removeDirectory,
  writeText
} from "./tooling-support.mjs";

/*
  Map manifest-declared relative source paths into absolute filesystem paths once,
  so every later helper can stay focused on validation or publishing rather than
  repeating path-join logic inline.
*/
function resolveSourcePaths(repoRoot, relativePaths) {
  return relativePaths.map((relativePath) => path.join(repoRoot, relativePath));
}

/* Fail early when the manifest points at missing CSS or JS source files. */
function assertSourceFilesExist(label, paths) {
  const missing = paths.filter((targetPath) => !pathExists(targetPath));
  if (missing.length) {
    throw new Error(`Missing FNLLA Web ${label} source files: ${missing.join(", ")}`);
  }
}

/*
  Concatenate source modules exactly in manifest order.

  The framework intentionally publishes readable runtime files instead of bundling
  through a minifier or module graph. That keeps diffs reviewable, makes browser
  debugging less opaque, and lets validation reason about one explicit ordering
  contract declared in scripts/fnlla-web-manifest.mjs.
*/
function getPublishedContent(paths, banner = "") {
  const body = paths.map((targetPath) => readText(targetPath).trimEnd()).join("\r\n\r\n");
  return banner
    ? `${banner.trimEnd()}\r\n\r\n${body}\r\n`
    : `${body}\r\n`;
}

/*
  Read the semantic version marker from VERSION.

  Several maintainers scripts, release notes and generated exports depend on one
  shared version value. Failing here is preferable to quietly producing assets
  with a missing or ambiguous version state.
*/
function getCurrentVersion(repoRoot) {
  const versionLines = readText(path.join(repoRoot, "VERSION")).split(/\r?\n/);
  const version = (versionLines[0] || "").trim();

  if (!version) {
    throw new Error("VERSION is empty");
  }

  return version;
}

function getRepositoryManifest(version, manifest) {
  return {
    schema_version: 1,
    product: {
      name: manifest.project.name,
      slug: manifest.project.slug,
      version,
      owner: manifest.project.owner,
      origin: manifest.project.origin,
      repository: manifest.project.repository,
      source_of_truth: manifest.project.sourceOfTruth
    },
    runtime: {
      css_entrypoint: manifest.runtime.cssOutput,
      js_entrypoint: manifest.runtime.jsOutput,
      icons_directory: "assets/icons",
      distribution_root: manifest.runtime.distRoot,
      release_files: manifest.runtime.distStaticFiles
    },
    release: {
      channel: "stable",
      state_files: manifest.release.stateFiles
    }
  };
}

function getRepositoryManifestContent(version, manifest) {
  return `${JSON.stringify(getRepositoryManifest(version, manifest), null, 2)}\n`;
}

/*
  Generate the README copied into the downstream dist handoff.

  This file is intentionally short: downstream teams need a stable explanation of
  the runtime contract, not a mirror of maintainer-only docs. The repository root
  remains the source of truth for operational guidance and governance files.
*/
function getDistReadmeContent(version) {
  return `# FNLLA Web runtime export

This directory is the runtime-only FNLLA Web handoff for downstream projects.

It contains the same supported browser runtime contract that lives under the repository root \`assets/\` tree, but without maintainer source files, docs chrome or repository-only working materials.

## Included files

- \`assets/css/fnlla-web.css\`
- \`assets/js/fnlla-web.js\`
- \`assets/icons/\`
- \`MANIFEST.json\`
- \`LICENSE.md\`
- \`SUPPORT.md\`
- \`TRADEMARKS.md\`
- \`VERSION\`

## How to use it

1. Copy this directory into the downstream project.
2. Link \`assets/css/fnlla-web.css\` from the page head.
3. Load \`assets/js/fnlla-web.js\` near the end of \`body\`.
4. Keep \`assets/icons/\` next to the runtime so local icon paths continue to work.

## Version

${version}

## Maintainer notes

- This export is generated by scripts/publish-fnlla-web.mjs.
- Do not edit files in dist/fnlla-web/ by hand; change src/ or the published runtime files first.
- Current framework metadata remains governed by README.md, VERSION, LICENSE.md, SUPPORT.md and TRADEMARKS.md in the repository root.
- Support and trademark boundaries remain governed by SUPPORT.md and TRADEMARKS.md in the repository root.
- Machine-readable runtime metadata remains governed by MANIFEST.json in the repository root.
- The repository root remains the authoritative maintainer workspace and documentation source of truth.
`;
}

/*
  Rebuild dist/fnlla-web/ from the published runtime snapshot.

  The dist export is not a separate build target with different semantics; it is
  a curated copy of the already-published runtime plus the minimum release files
  that downstream consumers need for traceability. Rebuilding it from scratch each
  time prevents stale files from surviving a framework refactor.
*/
export function writeRuntimeExport(options = {}) {
  const repoRoot = options.repoRoot || getRepoRoot(import.meta.url);
  const manifest = options.manifest || getFnllaWebManifest();
  const version = options.version || getCurrentVersion(repoRoot);
  const distRootPath = options.distRootPath || path.join(repoRoot, manifest.runtime.distRoot);

  removeDirectory(distRootPath);
  copyDirectory(path.join(repoRoot, "assets"), path.join(distRootPath, "assets"));

  manifest.runtime.distStaticFiles.forEach((relativePath) => {
    writeText(
      path.join(distRootPath, relativePath),
      readText(path.join(repoRoot, relativePath))
    );
  });

  writeText(path.join(distRootPath, "README.md"), getDistReadmeContent(version));
  return distRootPath;
}

/*
  Publish every generated framework surface in one ordered pass.

  Order matters:
  1. Validate that declared source inputs exist.
  2. Publish runtime CSS and JS.
  3. Publish docs-only JS that depends on source docs modules.
  4. Rebuild top-level docs shells and generated guide pages.
  5. Refresh the dist export from the newly published runtime.

  Keeping this sequencing centralized helps eliminate maintenance drift between
  "what the repo says is published" and "what downstream projects actually get".
*/
export function publishFramework(options = {}) {
  const repoRoot = options.repoRoot || getRepoRoot(import.meta.url);
  const manifest = getFnllaWebManifest();
  const cssTargetPath = path.join(repoRoot, manifest.runtime.cssOutput);
  const jsTargetPath = path.join(repoRoot, manifest.runtime.jsOutput);
  const docsJsTargetPath = path.join(repoRoot, manifest.docs.assets.jsOutput);
  const manifestTargetPath = path.join(repoRoot, "MANIFEST.json");
  const distRootPath = options.distRootPath || path.join(repoRoot, manifest.runtime.distRoot);
  const cssSourceFiles = resolveSourcePaths(repoRoot, manifest.source.css);
  const jsSourceFiles = resolveSourcePaths(repoRoot, manifest.source.js);
  const docsJsSourceFiles = resolveSourcePaths(repoRoot, manifest.docs.assets.js);
  const version = getCurrentVersion(repoRoot);

  assertSourceFilesExist("CSS", cssSourceFiles);
  assertSourceFilesExist("JavaScript", jsSourceFiles);
  assertSourceFilesExist("docs JavaScript", docsJsSourceFiles);

  writeText(cssTargetPath, getPublishedContent(cssSourceFiles, manifest.runtime.cssBanner));
  writeText(jsTargetPath, getPublishedContent(jsSourceFiles));
  writeText(docsJsTargetPath, getPublishedContent(docsJsSourceFiles));
  writeText(manifestTargetPath, getRepositoryManifestContent(version, manifest));
  syncDocShells({ repoRoot, manifest, check: false });
  buildGuidePages({ repoRoot, check: false });
  writeRuntimeExport({ repoRoot, manifest, version, distRootPath });

  return {
    cssTargetPath,
    jsTargetPath,
    docsJsTargetPath,
    manifestTargetPath,
    distRootPath
  };
}

/* Keep CLI output human-readable so local release work is easy to audit. */
function runCli() {
  const result = publishFramework();
  console.log(`Published FNLLA Web manifest: ${result.manifestTargetPath}`);
  console.log(`Published FNLLA Web runtime CSS: ${result.cssTargetPath}`);
  console.log(`Published FNLLA Web runtime JS: ${result.jsTargetPath}`);
  console.log(`Published FNLLA Web docs JS: ${result.docsJsTargetPath}`);
  console.log(`Refreshed FNLLA Web runtime export: ${result.distRootPath}`);
}

if (isDirectExecution(import.meta.url)) {
  try {
    runCli();
  } catch (error) {
    console.error(error && error.message ? error.message : String(error));
    process.exitCode = 1;
  }
}
